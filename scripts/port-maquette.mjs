/**
 * Ports the trilingual copy and the pre-diagnosis questions from the maquette
 * (site-maquette/index.html) into the app's source of truth:
 *   - messages/{fr,ar,en}.json  ← the `T` dictionary
 *   - lib/prediag.data.ts       ← the `Q` questions array
 *
 * The maquette is the source of truth (see CLAUDE.md). Re-run after any change
 * to the maquette copy:  node scripts/port-maquette.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const html = fs.readFileSync(path.join(root, 'site-maquette/index.html'), 'utf8');

const scriptBody = html.slice(html.indexOf('<script>') + 8, html.lastIndexOf('</script>'));

const LOCALES = ['fr', 'ar', 'en'];

// --- Extract the `T` dictionary (from `const T={};` up to `function applyI18n`) ---
const tStart = scriptBody.indexOf('const T={};');
const tEnd = scriptBody.indexOf('function applyI18n');
const tSource = scriptBody.slice(tStart, tEnd);

// --- Extract the `Q` questions (from `const Q={fr:[` up to `let QUESTIONS`) ---
const qStart = scriptBody.indexOf('const Q={fr:[');
const qEnd = scriptBody.indexOf('let QUESTIONS');
const qSource = scriptBody.slice(qStart, qEnd);

const sandbox = {};
vm.createContext(sandbox);
vm.runInContext(`${tSource}\n${qSource}\nthis.__T=T; this.__Q=Q;`, sandbox);

const T = sandbox.__T;
const Q = sandbox.__Q;

// --- Write message catalogs (one flat JSON per locale) ---
const messagesDir = path.join(root, 'messages');
fs.mkdirSync(messagesDir, { recursive: true });

const keys = Object.keys(T).sort();
for (const locale of LOCALES) {
  const out = {};
  for (const key of keys) {
    out[key] = T[key][locale] ?? T[key].fr;
  }
  fs.writeFileSync(
    path.join(messagesDir, `${locale}.json`),
    JSON.stringify(out, null, 2) + '\n',
    'utf8',
  );
  console.log(`messages/${locale}.json — ${keys.length} keys`);
}

// --- Write the typed pre-diagnosis data module (only once lib/prediag.ts exists) ---
const libDir = path.join(root, 'lib');
fs.mkdirSync(libDir, { recursive: true });

if (!fs.existsSync(path.join(libDir, 'prediag.ts'))) {
  console.log('lib/prediag.ts not found — skipping prediag.data.ts (generated in Part 6).');
  process.exit(0);
}

const prediagData = `// AUTO-GENERATED by scripts/port-maquette.mjs from site-maquette/index.html.
// Do not edit by hand. The maquette is the source of truth for questions & options.
// Re-run: node scripts/port-maquette.mjs
import type { LocalizedQuestions } from './prediag';

export const QUESTIONS: LocalizedQuestions = ${JSON.stringify(Q, null, 2)};
`;
fs.writeFileSync(path.join(libDir, 'prediag.data.ts'), prediagData, 'utf8');
console.log(`lib/prediag.data.ts — ${Q.fr.length} questions × ${LOCALES.length} locales`);
